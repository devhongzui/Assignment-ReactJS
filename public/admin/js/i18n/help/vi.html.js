<h2>Mẹo vận hành</h2>;
<p>
    Thao tác trên UI tương tự như file chuẩn của hệ điều hành giám đốc. Tuy
    nhiên, tính năng Kéo và Thả không thể thực hiện được với trình duyệt trên
    thiết bị di động.
</p>;
<ul>
    <li>Nhấp chuột phải hoặc nhấn lâu để hiển thị menu ngữ cảnh.</li>
    <li>
        Kéo và thả vào cây thư mục hoặc không gian làm việc hiện tại để di
        chuyển/sao chép mặt hàng.
    </li>
    <li>
        Lựa chọn mục trong không gian làm việc có thể được mở rộng lựa chọn bằng
        Shift hoặc Phím Alt (Tùy chọn).
    </li>
    <li>
        Kéo và thả vào thư mục đích hoặc không gian làm việc để tải tệp lên và
        thư mục.
    </li>
    <li>
        Hộp thoại tải lên có thể chấp nhận dán/thả dữ liệu clipboard hoặc danh
        sách URL và Kéo và thả từ trình duyệt hoặc trình quản lý tệp khác, v.v.
    </li>
    <li>
        Kéo bắt đầu bằng cách nhấn phím Alt(Option) để kéo ra trình duyệt bên
        ngoài. Nó sẽ trở thành hoạt động tải xuống với Google Chrome.
    </li>
</ul>;
